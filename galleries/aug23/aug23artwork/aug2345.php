                        <p>While reflecting on the book of Ruth, several components of &ldquo;hesed&rdquo; (God&apos;s faithfulness) stood out to me. The hesed of Ruth toward Naomi, clinging to her despite the hardships, is represented through Ruth taking the hand of Naomi in the centre. Boaz&apos;s finding favour in Ruth and his prayer of having God spread his wings over her is shown through the pair of wings on the top. Moreover, God consistently showed his hesed through his provision to the women. The contrast between the withered trees and the abundance of barley in Ruth&apos;s hands symbolizes the theme of fullness versus emptiness.</p>
                        <p><span html lang="zh-Hant">路得記中有些提到上帝信實的部分令我印象深刻。路得對拿俄米的「不顧困境、追隨到底」的忠誠以畫中間路得牽著拿俄米的方式呈現。畫上方的翅膀表達波阿斯賜恩給路得並祈禱她能「投靠耶和華以色列神的翅膀下」。此外、上帝以賜福與這兩位婦女來貫徹祂的信實。兩旁枯萎的樹和豐富的麥形成對比，象徵了福杯滿溢與空無所有的差異。</span></p>

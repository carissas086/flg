                        <h3 class="head-font aside-header">The Joyful Season of Lent</h3>
                        <p class="medium italics bold">Acrylic Painting</p>
                        <p>Lent is our yearly reminder that God is the one preparing the feast, a banquet better than we could ever make ourselves. We are invited through our Lenten observances to focus our thoughts, our appetites, and our hearts on the One who generously pours out &ldquo;a good measure, packed together, shaken down, and overflowing.&rdquo; (Luke 6:38 NAB). Lent reminds us that the many joys of this world bear no comparison to the joy that we shall find in the resurrection. Throughout the 40 days of Lent, the Church speaks of joy in the liturgy, illustrating via both Scripture and prayer that Christian joy is about anticipating God&apos;s presence:</p>
                        <div class="verse-box">
                            <p class="verse"><sup>24 </sup>This is the day that the Lord has made; let us rejoice and be glad in it.</p>
                            <p class="ref">Psalm 118:24 [ESV]</p>
                        </div>
                        <div class="verse-box">
                            <p class="verse"><sup>11 </sup>You will show me the way of life, granting me the joy of your presence and the pleasures of living with you forever.</p>
                            <p class="ref">Psalm 16:11 [NLT]</p>
                        </div>
                        <p>Peter actually said we could rejoice because we had a &ldquo;lively hope by the resurrection of Jesus Christ from the dead&rdquo; (1 Peter 1: 3 KJV). Our Lenten prayer, fasting, and almsgiving help us remember the merciful love and compassion that God shows all people and prepare us to receive salvation with joy.</p>

                        <p class="medium italics bold flg65-medium">Acrylic Painting &amp; Poetry</p>
                        <p class="flg65-centre"><span class="italics">This work is a joint collaboration between Angie Torres and Seth Mendoza</span></p>
                        <p>The first component of this work comes in the form of a poetic reimagining of Mary&apos;s time of praise in the book of Luke. God&apos;s Magnificence serves as a central trait of his character, one that Mary exalts and &ldquo;magnifies&rdquo; for Him in her prayer and rejoicing.</p>
                        <p>Within this writing we visualize each of the magnified traits we know of God&apos;s character with the components of the second portion of the work, the painting. The colours, setpieces and scaling of what we see on canvas reflects what we feel when reading of His glory.</p>
                        <p>The final portion of poetry was crafted from the question of whether Mary would have known Jesus&apos; ultimate fate on the cross. There is a cultural implication that suggests, among the disciples&apos; recorded interactions with Jesus, there would be women among them; and among them, Mary as well. Perhaps Mary knew, perhaps she didn&apos;t. Whether there was Christ or angel or no one at all to tell her, she praised Him all the same.</p>

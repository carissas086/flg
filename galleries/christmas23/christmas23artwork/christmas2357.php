                        <p>&ldquo;Glory to the Father, and to the Son, and to the Holy Spirit, as it was in the beginning, is now, and will be forever. Amen&rdquo;</p>
                        <p>Prayer, Simplicity, Scripture, Generosity, Hospitality, Community, Solitude, Fasting, and Sabbath are 9 Spiritual Habits of &ldquo;The Way&rdquo; Jesus lived.</p>
                        <p>Jesus&apos; mother Mary is wearing a maternity belt modeled on Our Lady of Guadelupe in Mexico (1531) and her head is bent in reverence with her hands pointing upward in prayer to God the Father.</p>
                        <p>The last lines of the Magnificat have always been a special prayer for me growing up. I have enjoyed meditating on all the musical versions of the Magnificat in preparation for this painting, and fellowshipping with my Spiritual Habits small group.</p>

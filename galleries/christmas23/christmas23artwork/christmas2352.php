                        <h3 class="head-font aside-header">Reflection on Magnificat</h3>
                        <p class="medium italics bold">Acrylic Painting</p>
                        <p>Through Mary&apos;s song it reminds me to rejoice in what God has done through Jesus Christ, acknowledge that everything we have is of Grace&apos;s grace and declare the victory of God over the proud and powerful. I should connect with what God is doing, not just this generation, but also in his salvation history that spans the distant past and looks ahead to a glorious future.</p>
                        <p>There is no pride or vanity in Mary&apos;s prayer because the focus is all on God. It is the prayer of a woman who knows her own limitations and who is in awe of a loving, all-powerful and liberating God. Today, Mary&apos;s vision of a just God - for whom nothing is impossible &mdash; can help us to live out our Christian vocation in an often unjust, secular society.</p>

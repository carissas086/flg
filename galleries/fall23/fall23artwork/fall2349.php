                        <h3 class="head-font aside-header">The Powerhouse of Thriving in Exile</h3>
                        <p class="medium italics bold">Acrylic on Canvas</p>
                        <p>When I was reading Daniel&apos;s prayer from the book of Daniel chapter 9, these phrases stood out to me: &ldquo;&hellip;understood from the Scriptures &hellip; Lord, forgive&hellip; praying, confessing my sin&hellip; for his holy hill&hellip; in prayer, Gabriel&hellip; instructed me&hellip;, &apos;Daniel, I have now come to give you insight and understanding&hellip;&rdquo;.</p>
                        <p>I reflect as I paint. Being a Christian often feels like living in exile among various cultures, political perspectives, and social influences, which might diverge from God&apos;s way. May our intimate relationships with God thrive like Daniel: to understand God&apos;s word, to confess our sins for His forgiveness, to be sanctified, to perceive insight and understanding of God&apos;s revelation, and to respond based on God&apos;s desire.</p>
